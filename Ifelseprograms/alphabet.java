class alphabet{

public static void main(String [] args){
	
	char ch='5';
	if(ch>='A'&&ch<='Z'){
	System.out.println("Good Morning");
	}
	else if(ch>='0'&&ch<='9'){
	System.out.println("Good Afternoon");
	}
	else if(!(ch>='A'&&ch<='Z')){
	System.out.println("Good Evening");
	}
	else{
	System.out.println("Good Bye");
	}

}











}